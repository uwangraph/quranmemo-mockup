export function createI18n() {
    let locale = $state('en');
    function t(key) { return dicts[locale]?.[key] || dicts['id']?.[key] || key; }
    function getDir() { return locales.find(l => l.code === locale)?.dir || 'ltr'; }
    return {
        get locale() { return locale; },
        set locale(v) { locale = v; },
        t, getDir
    };
}

